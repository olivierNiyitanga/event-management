    private void addTableRowData(){
        tableModel.setRowCount(0);
        eventDao dao = new eventDao();
        List<event> events = dao.allevents();
        for(event theevent: events){
            tableModel.addRow(new Object[]{
                theevent.getEventid(),
                theevent.getEventname(),
                theevent.getDate()
            });
        }
    }
    
