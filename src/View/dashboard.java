    private void jButton23ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton23ActionPerformed
        eventDao daoToRetrieve = new eventDao();
        List<event> eventList = daoToRetrieve.allevents();

        // Clear the table
        tableModel.setRowCount(0);

        if (!eventList.isEmpty()) {
            for (event event : eventList) {
                // Add rows to the table model
                tableModel.addRow(new Object[]{event.getEventid(), event.getEventname(), event.getDate()});
            }
        } else {
            
            JOptionPane.showMessageDialog(this, "No events found.");
        }
    }//GEN-LAST:event_jButton23ActionPerformed

