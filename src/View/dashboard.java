    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    if (jTextField1.getText().trim().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Event ID is mandatory", "Field Mandatory", JOptionPane.WARNING_MESSAGE);
} else if (jTextField2.getText().trim().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Event name is mandatory", "Field Mandatory", JOptionPane.WARNING_MESSAGE);
} else if (jDateChooser1.getDate() == null) {
    JOptionPane.showMessageDialog(this, "Date is mandatory", "Field Mandatory", JOptionPane.WARNING_MESSAGE);
} else {
    // Check if event ID is 3 characters
    if (jTextField1.getText().length() == 3) {
        // Call model class
        
        event theEvent = new event();
        theEvent.setEventid(jTextField1.getText());
        theEvent.setEventname(jTextField2.getText());
         SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
       String received_date=dateFormat.format(jDateChooser1.getDate());
       theEvent.setDate(received_date);

        // Call DAO
        eventDao dao = new eventDao();
        String feedback = dao.registerEvent(theEvent);
        JOptionPane.showMessageDialog(this, feedback);
    } else {
        JOptionPane.showMessageDialog(this, "Event ID should have exactly 3 characters", "Event ID Characters", JOptionPane.WARNING_MESSAGE);
    }
}

     
    }//GEN-LAST:event_jButton1ActionPerformed

